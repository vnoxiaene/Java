package entities;

public class conta {
    private String nome;
    private String numConta;
    private int saldo = 2090;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNumConta() {
        return numConta;
    }

    public void setNumConta(String numConta) {
        this.numConta = numConta;
    }

    public int getSaldo() {
        return saldo;
    }


}
